# variables are case sensitive
a0 = "App"
A0 = "APP"

# multiple variables can be assigned at a time
A, B, C = "Apple", "Ball", "Cat"

# multiple variables can be assigned with same value

a = b = c = "Dog"

print(a0)
print(A0)

print(A)
print(B)
print(C)

# print with a new line
print("\n" + a)
# print with a slash
print("\\" + b)
# print with within single quote
print("\'" + c + "\'")
# print with within double quote
print("\"" + c + "\"")
